            <h1>Section 1: GUI-based Processor Format Documents</h1>
            
            <h2>What is a GUI Word Processor?</h2>
            <p>A Graphical User Interface (GUI) word processor allows you to create, edit, format, and print documents using visual tools rather than code commands. Examples include Microsoft Word, Google Docs, and LibreOffice Writer.</p>
            

            <h2>The Ribbon Interface</h2>
            <ul>
                <li><strong>Home Tab:</strong> Fonts (Bold, Italic), Alignment (Left, Center), Bullets.</li>
                <li><strong>Insert Tab:</strong> Adding Images, Tables, Shapes, and Headers.</li>
                <li><strong>Layout Tab:</strong> Margins, Orientation (Portrait/Landscape), Columns.</li>
                <li><strong>File Menu:</strong> Saving, Exporting to PDF, Printing.</li>
            </ul>

            <div class="important-note">
                <h3>Visual Tip</h3>
                <p>In modern GUIs, "What You See Is What You Get" (WYSIWYG). The screen display closely mimics the final printed output.</p>
            </div>
        </div>

