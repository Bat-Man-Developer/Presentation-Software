            <div class="hands-on-section">
                <div class="hands-on-title">Exercise 2: The Smart Budget</div>
                
                <ol>
                    <li>Open a new Spreadsheet.</li>
                    <li><strong>Step 1 (Setup):</strong> Create headers in Row 1: <em>Item, Cost, Quantity, Total</em>.</li>
                    <li><strong>Step 2 (Data):</strong> Enter 3 items (e.g., Laptop, Mouse, Monitor) with costs and quantities.</li>
                    <li><strong>Step 3 (AI Help):</strong> Ask ChatGPT: <em>"In Excel, I have Cost in B2 and Quantity in C2. What formula goes in D2 to find the total? Also, how do I drag it down?"</em></li>
                    <li><strong>Step 4 (Apply):</strong> Enter the formula <code>=B2*C2</code> in D2 and use the fill handle to drag it down.</li>
                    <li><strong>Step 5 (Sum):</strong> Use the AutoSum button (Σ) or type <code>=SUM(D2:D4)</code> to calculate the grand total.</li>
                    <li><strong>Step 6 (Format):</strong> Select the cost columns and click the Currency ($) button.</li>
                </ol>
