            <h2>Navigating the Internet Visually</h2>
            <p>Web browsers (Chrome, Edge, Firefox) are the GUI window to the World Wide Web.</p>
            

            <h2>Essential Browser Features</h2>
            <ul>
                <li><strong>Address Bar (Omnibox):</strong> Type specific URLs (www.google.com) or search queries directly.</li>
                <li><strong>Tabs:</strong> Multitask by keeping multiple pages open in one window.</li>
                <li><strong>Bookmarks/Favorites:</strong> Save important sites for quick access later.</li>
                <li><strong>History:</strong> Retrace your steps to find previously visited pages.</li>
                <li><strong>Extensions:</strong> Small software modules that customize the browsing experience (e.g., AdBlock, Grammarly).</li>
            </ul>
