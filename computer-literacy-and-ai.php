            <h1>Section 2: GUI-based Spreadsheet Application</h1>
            
            <h2>Understanding the Grid Interface</h2>
            <p>Spreadsheets (Excel, Google Sheets) organize data into a grid of Cells, defined by Rows (Numbers) and Columns (Letters).</p>
            

            <h2>Key GUI Elements</h2>
            <ul>
                <li><strong>Cell Address:</strong> The intersection of a column and row (e.g., A1, C5).</li>
                <li><strong>Formula Bar:</strong> The area at the top where you type equations.</li>
                <li><strong>Sheet Tabs:</strong> Navigate between different datasets within one file.</li>
                <li><strong>Formatting Tools:</strong> Currency ($), Percent (%), and Conditional Formatting.</li>
            </ul>

            <div class="demo-box">
                <h3>Data Types</h3>
                <p>Spreadsheets distinguish between <strong>Text</strong> (Labels), <strong>Numbers</strong> (Values), and <strong>Formulas</strong> (Calculations).</p>
            </div>
        </div>

