            <h1>Ethics and Data Privacy (Ukukhusela ulwazi)</h1>
            <p>Administrators handle highly sensitive company and employee data. Using public AI tools requires strict adherence to privacy rules.</p>

            <img src="assets/images/ai-for-secretaries-and-admin/9.png" alt="Secure digital lock protecting sensitive corporate and employee data" class="slide-image">

            <h2>The Golden Rules of AI Privacy:</h2>
            <ul>
                <li><strong>Never input PII:</strong> Do not paste ID numbers, home addresses, or private phone numbers into public AI tools.</li>
                <li><strong>Anonymize financial data:</strong> If asking AI to format a budget, use generic numbers or remove company names.</li>
                <li><strong>Check company policy:</strong> Always use enterprise-approved AI tools if your company provides them (e.g., Copilot for Microsoft 365).</li>
            </ul>
        </div>

