            <h1>Traditional Automation vs. Generative AI</h1>
            <p>Understanding the difference between older tools and modern AI is crucial for administrators.</p>

            <img src="assets/images/ai-for-secretaries-and-admin/3.png" alt="Diagram comparing traditional rule-based automation with modern generative AI" class="slide-image">

            <div class="two-column">
                <div class="highlight">
                    <h3>Traditional Automation</h3>
                    <p>Follows strict rules. Example: Automatically moving emails with the word "Invoice" to a specific folder.</p>
                    <p>It cannot think; it only executes.</p>
                </div>
                <div class="highlight" style="border-left-color: var(--secondary-color);">
                    <h3>Generative AI</h3>
                    <p>Understands context. Example: Reading an email, understanding the client is upset, drafting an empathetic response, and suggesting a meeting time.</p>
                    <p>It can create, summarize, and adapt.</p>
                </div>
