            <h2>Evolution of AI in HR</h2>
            <ul>
                <li>From manual paper-based processes to digital automation</li>
                <li>Timeline: 1990s databases → 2000s HRIS → 2010s cloud → 2020s AI</li>
                <li>Key milestones: ATS systems, predictive analytics, machine learning</li>
                <li>Current trends: Natural language processing and conversational AI</li>
                <li>Future outlook: Hyper-personalization and autonomous HR systems</li>
            </ul>

            <h2>Types of AI in HR</h2>
            <div class="two-column">
                <div>
                    <h3>Machine Learning Applications</h3>
                    <ul>
                        <li>Predictive analytics for talent management</li>
                        <li>Pattern recognition in employee behavior</li>
                        <li>Automated resume screening and ranking</li>
                        <li>Performance prediction models</li>
                    </ul>
                </div>
                <div>
                    <h3>Natural Language Processing</h3>
                    <ul>
                        <li>Chatbots for employee interactions</li>
                        <li>Sentiment analysis of feedback</li>
                        <li>Automated documentation and transcription</li>
                        <li>Language translation for global teams</li>
                    </ul>
                </div>
            </div>

            <h2>Key AI Concepts & Terminology</h2>
            <ul>
                <li><strong>Artificial Intelligence:</strong> Computer systems performing tasks requiring human intelligence</li>
                <li><strong>Machine Learning:</strong> Algorithms that improve through experience without explicit programming</li>
                <li><strong>Deep Learning:</strong> Neural networks with multiple layers for complex pattern recognition</li>
                <li><strong>Natural Language Processing:</strong> AI's ability to understand and generate human language</li>
                <li><strong>Algorithms:</strong> Step-by-step instructions for solving problems or performing tasks</li>
                <li><strong>Training Data:</strong> Information used to teach AI systems how to make decisions</li>
                <li><strong>Bias:</strong> Systematic errors in AI decision-making due to flawed training data</li>
            </ul>
        </div>

