        import pulp
        import pandas as pd
        import numpy as np

        # Create optimization problem
        prob = pulp.LpProblem("Workforce_Optimization", pulp.LpMaximize)

        # Define decision variables
        # x[i,j] = 1 if employee i is assigned to project j
        employees = ['John', 'Jane', 'Bob', 'Alice']
        projects = ['ProjectA', 'ProjectB', 'ProjectC']

        x = pulp.LpVariable.dicts("assign", 
                                [(i,j) for i in employees for j in projects],
                                cat='Binary')

        # Objective function: Maximize satisfaction + skill match
        satisfaction = {'John': {'ProjectA': 0.8, 'ProjectB': 0.6, 'ProjectC': 0.9}}
        # ... (add satisfaction scores for all employees and projects)

        prob += pulp.lpSum([satisfaction[i][j] * x[i,j] 
                        for i in employees for j in projects])

        # Constraints
        # Each project needs exactly one person
        for j in projects:
            prob += pulp.lpSum([x[i,j] for i in employees]) == 1

        # Each employee assigned to at most one project
        for i in employees:
            prob += pulp.lpSum([x[i,j] for j in projects]) <= 1

        # Solve the problem
        prob.solve()

        # Extract results
        for i in employees:
            for j in projects:
                if x[i,j].varValue == 1:
                    print(f"Assign {i} to {j}")
                </code></pre>
