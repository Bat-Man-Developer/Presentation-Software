            # Make predictions
            predictions = aml.leader.predict(test_h2o)
            
            return aml.leader, predictions

        # Ensemble prediction combining Prophet and AutoML
        def ensemble_prediction(prophet_forecast, automl_prediction, weights=[0.6, 0.4]):
